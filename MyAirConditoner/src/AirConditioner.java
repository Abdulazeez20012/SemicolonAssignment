public class AirConditioner {

    private boolean power = false;

    public boolean power() {
        return power;
    }

    public void isOn() {
        power = true;
    }

    public void isOff() {
        power = true;
    }

    public void powerOff() {
        power = false;
    }
    public void powerOn() {
        power = false;
    }
}
