    public static void printSevenSegmentDisplay(char[][] display) {
        if (display == null) {
            System.out.println("Invalid Input");
            return;
        }
        for (char[] row : display) {
            for (char c : row) {
                System.out.print(c + " ");
            }
            System.out.println();
        }
    }

    public static void main(String[] args) {
        int number = 4000;
        char[][] display = getSevenSegmentDisplay(number);
        printSevenSegmentDisplay(display);
    }
